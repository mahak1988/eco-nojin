    '        ',
    '        for model_name in self.config.models:',
    '            if model_name in self._models:',
    '                model = self._models[model_name]',
    '                if hasattr(model, "run"):',
    '                    model_results = model.run(inputs)',
    '                    results[model_name] = model_results',
    '        ',
    '        return results',
    '    ',
    '    def _run_iterative(self, inputs: Dict[str, Any]) -> Dict[str, Any]:',
    '        """Run models iteratively until convergence"""',
    '        results = {}',
    '        current_inputs = inputs.copy()',
    '        ',
    '        for iteration in range(self.config.max_iterations):',
    '            iteration_results = {}',
    '            ',
    '            for model_name in self.config.models:',
    '                if model_name in self._models:',
    '                    model = self._models[model_name]',
    '                    if hasattr(model, "run"):',
    '                        model_results = model.run(current_inputs)',
    '                        iteration_results[model_name] = model_results',
    '                        current_inputs.update(model_results)',
    '            ',
    '            # Check convergence (simplified)',
