export function gameText(s: GameStrings, key: string): string {
  return (s[key as keyof GameStrings] as string) ?? key;
}
export function tabText(s: GameStrings, t: TabKey): string {
  return s[`tab_${t}` as keyof GameStrings] as string;
}
export function diffText(s: GameStrings, d: DifficultyKey): string {
  return s[d];
}
export function localeOf(lang: GameLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
// روزهای مانده تا deadline — متن بومی
export function deadlineText(iso: string, lang: GameLang): { text: string; urgent: boolean; expired: boolean } {
  const s = GAME_STR[lang];
  const locale = localeOf(lang);
  const days = Math.ceil((new Date(iso).getTime() - Date.now()) / 86_400_000);
  if (days < 0) return { text: s.expired, urgent: false, expired: true };
  if (days === 0) return { text: s.lastDay, urgent: true, expired: false };
  return { text: s.deadlineIn.replace("{n}", days.toLocaleString(locale)), urgent: days <= 2, expired: false };
}