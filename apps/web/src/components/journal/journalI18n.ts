export function jrText(s: JournalStrings, key: string): string {
  return (s[key as keyof JournalStrings] as string) ?? key;
}
export function accountText(s: JournalStrings, key: string): string {
  return (s[key as keyof JournalStrings] as string) ?? key;
}
export function localeOf(lang: JrLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
export function formatMoney(amount: number, lang: JrLang): string {
  return new Intl.NumberFormat(localeOf(lang), { style: "currency", currency: "USD", maximumFractionDigits: 0 }).format(amount);
}
export function formatDate(iso: string, lang: JrLang): string {
  return new Date(iso).toLocaleDateString(localeOf(lang), { year: "numeric", month: "short", day: "numeric" });
}