export function ecoText(s: EcoStrings, key: string): string {
  return (s[key as keyof EcoStrings] as string) ?? key;
}
export function catText(s: EcoStrings, c: string): string {
  return (s[`cat_${c}` as keyof EcoStrings] as string) ?? c;
}
export function localeOf(lang: EcoLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
export function timeAgo(iso: string, lang: EcoLang): string {
  const s = ECO_STR[lang];
  const locale = localeOf(lang);
  const diff = Date.now() - new Date(iso).getTime();
  const mins = Math.floor(diff / 60_000);
  const hours = Math.floor(mins / 60);
  const days = Math.floor(hours / 24);
  const fmt = (n: number) => n.toLocaleString(locale);
  if (mins < 1) return s.justNow;
  if (mins < 60) return s.minsAgo.replace("{n}", fmt(mins));
  if (hours < 24) return s.hoursAgo.replace("{n}", fmt(hours));
  return s.daysAgo.replace("{n}", fmt(days));
}
export function shortAddr(addr: string): string {
  return addr.length > 12 ? `${addr.slice(0, 6)}…${addr.slice(-4)}` : addr;
}