export function libText(s: LibraryStrings, key: string): string {
  return (s[key as keyof LibraryStrings] as string) ?? key;
}
export function typeText(s: LibraryStrings, t: ResourceType): string {
  return s[`type_${t}` as keyof LibraryStrings] as string;
}
export function catText(s: LibraryStrings, c: CategoryKey): string {
  return s[c];
}
export function sortText(s: LibraryStrings, k: SortKey): string {
  return k === "popular" ? s.sortPopular : k === "title" ? s.sortTitle : s.sortNewest;
}
export function localeOf(lang: LibLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}