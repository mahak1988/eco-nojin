export function tabText(s: CommunityStrings, t: TabKey): string {
  return s[`tab_${t}` as keyof CommunityStrings] as string;
}
export function contentText(s: CommunityStrings, key: string): string {
  return (s[key as keyof CommunityStrings] as string) ?? key;
}
export function localeOf(lang: ComLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
export function timeAgo(iso: string, lang: ComLang): string {
  const s = COM_STR[lang];
  const locale = localeOf(lang);
  const diff = Date.now() - new Date(iso).getTime();
  const mins = Math.floor(diff / 60_000);
  const hours = Math.floor(mins / 60);
  const days = Math.floor(hours / 24);
  const fmt = (n: number) => n.toLocaleString(locale);
  if (mins < 1) return s.justNow;
  if (mins < 60) return s.minsAgo.replace("{n}", fmt(mins));
  if (hours < 24) return s.hoursAgo.replace("{n}", fmt(hours));
  return s.daysAgo.replace("{n}", fmt(days));
}