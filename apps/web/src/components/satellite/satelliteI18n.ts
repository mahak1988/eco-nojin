export function satText(s: SatelliteStrings, key: string): string {
  return (s[key as keyof SatelliteStrings] as string) ?? key;
}
export function layerText(s: SatelliteStrings, l: Layer): string {
  return s[`layer_${l}` as keyof SatelliteStrings] as string;
}
export function cloudFilterText(s: SatelliteStrings, f: CloudFilter): string {
  return f === "all" ? s.filterAll : (s[`filter_${f}` as keyof SatelliteStrings] as string);
}
export function localeOf(lang: SatLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}