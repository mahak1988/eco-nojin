export function eduText(s: EducationStrings, key: string): string {
  return (s[key as keyof EducationStrings] as string) ?? key;
}
export function levelText(s: EducationStrings, l: LevelKey): string {
  return s[l];
}
export function certStatusText(s: EducationStrings, st: CertStatusKey): string {
  return s[st];
}
export function localeOf(lang: EduLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
export function durationText(s: EducationStrings, h: number, m: number, lang: EduLang): string {
  const locale = localeOf(lang);
  const fh = h.toLocaleString(locale);
  const fm = m.toLocaleString(locale);
  return m > 0 ? s.durHM.replace("{h}", fh).replace("{m}", fm) : s.durH.replace("{h}", fh);
}
export function fmtDate(iso: string, lang: EduLang): string {
  return new Date(iso).toLocaleDateString(localeOf(lang), { year: "numeric", month: "long", day: "numeric" });
}