export function repText(s: ReportStrings, key: string): string {
  return (s[key as keyof ReportStrings] as string) ?? key;
}
export function typeText(s: ReportStrings, t: ReportType): string {
  return s[`type_${t}` as keyof ReportStrings] as string;
}
export function statusText(s: ReportStrings, st: ReportStatus): string {
  return s[`status_${st}` as keyof ReportStrings] as string;
}
export function periodText(s: ReportStrings, p: ReportPeriod): string {
  return s[`period_${p}` as keyof ReportStrings] as string;
}
export function unitText(s: ReportStrings, u: UnitKey): string {
  return s[u] as string;
}
export function localeOf(lang: RepLang): string {
  return lang === "fa" ? "fa-IR" : lang === "ar" ? "ar-EG" : "en-US";
}
// نام گزارش: seed → nameKey؛ generate → ترکیب type·period (با تغییر زبان به‌روز می‌شود)
export function reportName(r: Report, s: ReportStrings): string {
  return r.nameKey ? repText(s, r.nameKey) : `${typeText(s, r.type)} · ${periodText(s, r.period)}`;
}
// سند متنی گزارش (پیش‌نمایش/دانلود) — کنار متن‌ها، چون text-heavy است
export function buildReportDoc(r: Report, s: ReportStrings, locale: string): string {
  const tpl = REPORT_TEMPLATE[r.type];
  const name = reportName(r, s);
  const lines = [
    name,
    "=".repeat(Math.max(name.length, 12)),
    `${s.typeLabel}: ${typeText(s, r.type)}`,
    `${s.periodLabel}: ${periodText(s, r.period)}`,
    `${s.colStatus}: ${statusText(s, r.status)}`,
    `${s.colDate}: ${formatDate(r.date, locale)}`,
    `${s.statDownloads}: ${formatNumber(r.downloads, locale)}`,
    "",
    `${s.summaryLabel}:`,
    repText(s, tpl.summaryKey),
    "",
    `${s.metricsLabel}:`,
    ...tpl.metrics.map((m) => {
      const u = unitText(s, m.unitKey);
      return `  • ${repText(s, m.labelKey)}: ${formatNumber(m.value, locale)}${u ? " " + u : ""}`;
    }),
    "",
  ];
  return lines.join("\n");
}